package com.library.model;

import java.time.LocalDate;
import java.util.List;

public class Document {

    private int documentId;  // The ID for the document
    private String name;
    private List<Integer> authorIds;
    private List<Integer> tagIds;
    private int publisherId;
    private LocalDate datePublished;
    private LocalDate dateAdded;
    private int quantityCurrent;
    private int quantityTotal;
    private String isbn10;  // Optional
    private String isbn13;  // Preferred

    // Constructor to initialize the Document object
    public Document(int documentId, String name, List<Integer> authorIds, List<Integer> tagIds, 
    int publisherId, String isbn10, String isbn13, LocalDate datePublished, LocalDate dateAdded, 
                    int quantityCurrent, int quantityTotal) {
        this.documentId = documentId;
        this.name = name;
        this.authorIds = authorIds;
        this.tagIds = tagIds;
        this.publisherId = publisherId;
        this.isbn10 = isbn10;
        this.isbn13 = isbn13;
        this.datePublished = datePublished;
        this.dateAdded = dateAdded;
        this.quantityCurrent = quantityCurrent;
        this.quantityTotal = quantityTotal;
    }

    // Constructor for creating a new Document (without ID initially)
    public Document(String name, List<Integer> authorIds, List<Integer> tagIds, int publisherId,  String isbn10, String isbn13, 
                    LocalDate datePublished, LocalDate dateAdded, int quantityCurrent, int quantityTotal) {
        this.documentId = -1; // ID will be generated by the database
        this.name = name;
        this.authorIds = authorIds;
        this.tagIds = tagIds;
        this.publisherId = publisherId;
        this.isbn10 = isbn10;
        this.isbn13 = isbn13;
        this.datePublished = datePublished;
        this.dateAdded = dateAdded;
        this.quantityCurrent = quantityCurrent;
        this.quantityTotal = quantityTotal;
    }

    // Getters and setters
    public int getId() {
        return documentId;
    }

    public void setId(int id) {
        this.documentId = id;
    }
    public String getIsbn10() {
        return isbn10;
    }

    public void setIsbn10(String isbn10) {
        this.isbn10 = isbn10;
    }

    public String getIsbn13() {
        return isbn13;
    }

    public void setIsbn13(String isbn13) {
        this.isbn13 = isbn13;
    }
    public int getDocumentId() {
        return documentId;
    }

    public void setDocumentId(int documentId) {
        this.documentId = documentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Integer> getAuthorIds() {
        return authorIds;
    }

    public void setAuthorIds(List<Integer> authorIds) {
        this.authorIds = authorIds;
    }

    public List<Integer> getTagIds() {
        return tagIds;
    }

    public void setTagIds(List<Integer> tagIds) {
        this.tagIds = tagIds;
    }

    public int getPublisherId() {
        return publisherId;
    }

    public void setPublisherId(int publisherId) {
        this.publisherId = publisherId;
    }

    public LocalDate getDatePublished() {
        return datePublished;
    }

    public void setDatePublished(LocalDate datePublished) {
        this.datePublished = datePublished;
    }

    public LocalDate getDateAdded() {
        return dateAdded;
    }

    public void setDateAdded(LocalDate dateAdded) {
        this.dateAdded = dateAdded;
    }

    public int getQuantityCurrent() {
        return quantityCurrent;
    }

    public void setQuantityCurrent(int quantityCurrent) {
        this.quantityCurrent = quantityCurrent;
    }

    public int getQuantityTotal() {
        return quantityTotal;
    }

    public void setQuantityTotal(int quantityTotal) {
        this.quantityTotal = quantityTotal;
    }
}
